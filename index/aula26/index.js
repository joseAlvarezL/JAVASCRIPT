// Operadores de Comparação

// ==  // Compara os valores, ignorando os tipos.
// === // Compara valores e tipos, verifica igualdade estrita.
// !=  // Compara valores, ignorando os tipos, verifica diferença.
// !== // Compara valores e tipos, verifica diferença estrita.
// >   // Verifica se o valor da esquerda é maior que o da direita.
// <   // Verifica se o valor da esquerda é menor que o da direita.
// >=  // Verifica se o valor da esquerda é maior ou igual ao da direita.
// <=  // Verifica se o valor da esquerda é menor ou igual ao da direita.


let num1 = 3;
let nomeUm = 'Andre';


console.log(num1>0);
console.log(num1<9);
console.log(num1<=9);
console.log(num1>=9);

console.log(num1 === 3)
console.log(num1 !== 3)

console.log(nomeUm !== 'Andre')
console.log(nomeUm === 'Andre')